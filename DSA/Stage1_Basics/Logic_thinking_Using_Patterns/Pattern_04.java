package Stage1_Basics.Logic_thinking_Using_Patterns;
public class Pattern_04 {
    static void main() {
        int total=5;
        for(int i=1;i<=total;i++){
            for(int j=1;j<=total;j++){
                System.out.print("*");
            }
            System.out.println();
        }

    }
}
